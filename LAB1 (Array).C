#include<stdio.h>
#include<conio.h>
int a[10],n,i,pos,ele;

void create()
{
  printf("\nEnter the limit of array");
  scanf("%d",&n);
  if(n>10)
  {
   printf("\nInvalid limit");
   getch();
   return;
  }

  printf("\nEnter the values of array");
  for(i=0;i<n;i++)
  scanf("%d",&a[i]);
}
void insert()
{
 printf("\nEnter the position ");
 scanf("%d",&pos);
 printf("\nEnter element to be inserted ");
 scanf("%d",&ele);

 for(i=n-1;i>=pos;i--)
  a[i+1]=a[i];
  a[pos]=ele;
  n=n+1;
}

void del()
{
 printf("\nEnter the position to be deleted ");
 scanf("%d",&pos);
 ele=a[pos];
 for(i=pos;i<n-1;i++)
 a[i]=a[i+1];
 n=n-1;
 printf("\nElement deleted is %d",ele);
}



void display()
{
 printf("\nArray Elements are\n");
 for(i=0;i<n;i++)
 {
  printf("\na[%d]=%d",i,a[i]);
 }
}

void main()
{
 int ch=0;
 clrscr();
 do
 {
 printf("\n1.Create\t2.Display\t3.Insert\t4.Delete\t5.Exit");
 scanf("%d",&ch);
 switch (ch)
 {
  case 1:create();break;
  case 2:display();break;
  case 3:insert();break;
  case 4:del();break;
  case 5:exit();
 }
}
while(ch!=5);
getch();
}














































































































































































































































































































































































































































































































































































































































































































































































